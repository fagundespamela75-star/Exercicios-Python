# A estrutura de repetição for, é usada 
# para ler listas, tuplas, dicionarios ou sequencias
# Neste exemplo usamos uma sequencia com o comando
# range(intervalo) que vai de 1 até 10 e 11 é 
# o valor de saida

for i in range(1,11):      #11 não vai mostrar porq é a saida somente do 1 ao 10
    print(i)
